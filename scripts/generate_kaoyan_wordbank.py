def main():
    import argparse
    parser = argparse.ArgumentParser(description="考研核心词库自动生成器")
    parser.add_argument("--count", type=int, default=0, help="生成数量（0=全部）")
    parser.add_argument("--batch", type=str, default="", help="指定批次字母（如 A）")
    parser.add_argument("--no-api", action="store_true", help="跳过在线词典API，仅用内置释义")
    parser.add_argument("--output", type=str, default="", help="输出文件路径")
    args = parser.parse_args()

    # 筛选词汇
    word_list = list(all_words.items())
    if args.batch:
        letter = args.batch.upper()
        word_list = [(w, d) for w, d in word_list if w[0].upper() == letter]
    if args.count and args.count < len(word_list):
        word_list = word_list[:args.count]

    total = len(word_list)
    print(f"📚 考研核心词库生成器")
    print(f"   目标词汇: {total} 个")
    print(f"   在线查词: {'关闭' if args.no_api else '开启 (dictionaryapi.dev)'}")
    print()

    generated = []
    api_success = 0
    api_fail = 0

    for i, (word, cn_def) in enumerate(word_list):
        dict_data = None
        if not args.no_api:
            try:
                dict_data = fetch_dictionary(word)
                if dict_data:
                    api_success += 1
                else:
                    api_fail += 1
                # 礼貌限速，每词间隔0.5秒
                if (i + 1) % 5 == 0:
                    time.sleep(1.0)
                else:
                    time.sleep(0.3)
            except Exception:
                api_fail += 1

        entry = build_word_entry(word, cn_def, dict_data)
        generated.append(entry)

        # 进度显示
        ph = "🔊" if dict_data and dict_data.get("phonetic_uk") else "  "
        syn = "📎" if dict_data and dict_data.get("synonyms") else "  "
        status = f"{ph}{syn}"
        if (i + 1) % 20 == 0 or i == 0 or i == total - 1:
            print(f"  [{i+1:4d}/{total}] {status} {word}")

    print()
    print(f"✅ 生成完成: {len(generated)} 个词条")
    if not args.no_api:
        print(f"   🔊 获取到音标: {api_success} 词")
        print(f"   ❌ 未获取到: {api_fail} 词")

    # 保存
    output_path = Path(args.output) if args.output else KAOYAN_WORDS_FILE
    DATA_DIR.mkdir(parents=True, exist_ok=True)
    with open(output_path, "w", encoding="utf-8") as f:
        json.dump(generated, f, ensure_ascii=False, indent=2)

    print(f"📄 已保存至: {output_path}")

    # 提示下一步
    if output_path.name == "kaoyan.json":
        print()
        print("💡 下一步：运行导入脚本将词库写入数据库")
        print("   python scripts/init_data.py")
        print("   (或) python scripts/import_user_wordlist.py --file data/kaoyan.json")


if __name__ == "__main__":
    main()
