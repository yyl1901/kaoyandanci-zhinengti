FREE_DICT_API = "https://api.dictionaryapi.dev/api/v2/entries/en/"


def fetch_dictionary(word: str) -> dict | None:
    """从免费词典 API 获取单词数据（音标 + 英文释义 + 近义词）"""
    try:
        resp = httpx.get(FREE_DICT_API + word, timeout=10)
        if resp.status_code != 200:
            return None
        data = resp.json()
        if not data or not isinstance(data, list):
            return None
        entry = data[0]

        # 提取音标
        phonetic_uk = ""
        phonetic_us = ""
        for p in entry.get("phonetics", []):
            text = p.get("text", "")
            audio = p.get("audio", "")
            if not text:
                continue
            # 区分英美音
            if "uk" in str(audio).lower() or "uk" in str(p).lower():
                phonetic_uk = text
            elif "us" in str(audio).lower() or "us" in str(p).lower():
                phonetic_us = text
            elif not phonetic_uk:
                phonetic_uk = text  # 第一个默认为英音

        # 提取英文释义和近/反义词
        definitions = []
        synonyms = []
        antonyms = []
        for meaning in entry.get("meanings", []):
            pos = meaning.get("partOfSpeech", "")
            syns = meaning.get("synonyms", [])
            ants = meaning.get("antonyms", [])
            if syns:
                synonyms.extend(syns[:3])
            if ants:
                antonyms.extend(ants[:3])

            for d in meaning.get("definitions", []):
                en_def = d.get("definition", "")
                if en_def:
                    definitions.append({"pos": pos, "meaning": en_def})

        return {
            "phonetic_uk": phonetic_uk,
            "phonetic_us": phonetic_us,
            "definitions": definitions[:6],  # 最多6条英文释义
            "synonyms": list(set(synonyms))[:6],
            "antonyms": list(set(antonyms))[:6],
        }
    except Exception as e:
        return None


def parse_cn_definition(cn_def_str: str) -> list[dict]:
    """解析中文释义字符串为结构化定义"""
    # 例如: "v.放弃，抛弃；n.放纵，放任 【熟词僻义：放纵】"
    results = []
    # 移除备注
    cn_def_str = re.sub(r'【.*?】', '', cn_def_str).strip()
    # 按分号或换行分割
    parts = re.split(r'[;；]', cn_def_str)
    for part in parts:
        part = part.strip()
        if not part:
            continue
        # 尝试提取词性
        m = re.match(r'^([a-z]+\.)\s*(.+)', part, re.IGNORECASE)
        if m:
            results.append({"pos": m.group(1), "meaning": m.group(2).strip()})
        else:
            # 没有词性标注，继承上一个词性或留空
            results.append({"pos": "", "meaning": part})
    return results


def build_word_entry(word: str, cn_def: str, dict_data: dict | None) -> dict:
    """构建完整的单词词条"""
    cn_defs = parse_cn_definition(cn_def)

    # 合并中英文释义
    definition = json.dumps(cn_defs, ensure_ascii=False)

    # 处理音标
    phonetic_uk = ""
    phonetic_us = ""
    if dict_data:
        phonetic_uk = dict_data.get("phonetic_uk", "")
        phonetic_us = dict_data.get("phonetic_us", "")

    # 处理近反义词
    synonyms = []
    antonyms = []
    if dict_data:
        synonyms = dict_data.get("synonyms", [])
        antonyms = dict_data.get("antonyms", [])

    # 判断是否为熟词僻义
    category = "rare" if "熟词僻义" in cn_def else "core"

    # 估测考频（基于词长和常见度粗略估计，后续可调整）
    frequency = 4 if category == "core" else 5

    return {
        "word": word,
        "phonetic_uk": phonetic_uk,
        "phonetic_us": phonetic_us,
        "definition": definition,
        "exam_type": "kaoyan",
        "category": category,
        "frequency": frequency,
        "etymology": "",
        "example_sentences": "[]",
        "synonyms": json.dumps(synonyms, ensure_ascii=False) if synonyms else "[]",
        "antonyms": json.dumps(antonyms, ensure_ascii=False) if antonyms else "[]",
    }


